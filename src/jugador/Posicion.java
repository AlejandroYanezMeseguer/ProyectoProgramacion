package jugador;

/**
 * Clase Posicion que se utilizara para guardar las posiciones de las armas dentro del Array
 */
public class Posicion {

    public int x;
    public int y;

    /**
     *Constructor de la clase Posicion
     */
    public Posicion(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
